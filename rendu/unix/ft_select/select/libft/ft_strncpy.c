#include <string.h>

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	if (n == 0)
		return (s1);
	*s1 = *s2;
	if (*s2 != '\0')
		++s2;
	ft_strncpy(s1 + 1, s2, n - 1);
	return (s1);
}

