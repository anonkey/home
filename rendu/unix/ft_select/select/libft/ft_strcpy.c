char		*ft_strcpy(char *s1, const char *s2)
{
	if (*s2 == '\0')
	{
		*s1 = '\0';
		return (s1);
	}
	*s1 = *s2;
	ft_strcpy(s1 + 1, s2 + 1);
	return (s1);
}

